    const squares = [];

    // Créer le plateau de jeu
    function createBoard() {
        for (let i = 0; i < layout.length; i++) {
            const square = document.createElement("div");
            grid.appendChild(square);
            squares.push(square);

            // Ajouter les classes aux cases
            if (layout[i] === 0) {
                squares[i].classList.add("pac-dot");
            } else if (layout[i] === 1) {
                squares[i].classList.add("mur");
            } else if (layout[i] === 2) {
                squares[i].classList.add("fantome-lair");
            } else if (layout[i] === 3) {
                squares[i].classList.add("power-pellet");
            }
        }
    }

    createBoard();

    // Position initiale de Pac-Man
    let pacmanIndex = 490;
    squares[pacmanIndex].classList.add("pac-man");

    // Mouvements de Pac-Man
    function movePacman(e) {
        squares[pacmanIndex].classList.remove("pac-man");

        switch (e.key) {
            case "ArrowUp":
                if (!squares[pacmanIndex - width].classList.contains("mur") &&
                    !squares[pacmanIndex - width].classList.contains("fantome-lair")) {
                    pacmanIndex -= width;
                }
                break;
            case "ArrowDown":
                if (!squares[pacmanIndex + width].classList.contains("mur") &&
                    !squares[pacmanIndex + width].classList.contains("fantome-lair")) {
                    pacmanIndex += width;
                }
                break;
            case "ArrowLeft":
                if (!squares[pacmanIndex - 1].classList.contains("mur") &&
                    !squares[pacmanIndex - 1].classList.contains("fantome-lair") &&
                    pacmanIndex % width !== 0) {
                    pacmanIndex -= 1;
                }
                break;
            case "ArrowRight":
                if (!squares[pacmanIndex + 1].classList.contains("mur") &&
                    !squares[pacmanIndex + 1].classList.contains("fantome-lair") &&
                    pacmanIndex % width < width - 1) {
                    pacmanIndex += 1;
                }
                break;
        }

        squares[pacmanIndex].classList.add("pac-man");

        // Pac-Man mange un pac-dot
        if (squares[pacmanIndex].classList.contains("pac-dot")) {
            squares[pacmanIndex].classList.remove("pac-dot");
            score++;
            scoreDisplay.innerText = score;
        }

        // Pac-Man mange un power-pellet
        if (squares[pacmanIndex].classList.contains("power-pellet")) {
            squares[pacmanIndex].classList.remove("power-pellet");
            score += 10;
            scoreDisplay.innerText = score;
            // Ajouter une fonctionnalité pour rendre les fantômes effrayés
        }
    }

    document.addEventListener("keydown", movePacman);
